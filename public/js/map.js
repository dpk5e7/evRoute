function setMapMarkers(map) {
  const stationData = getStationData();

  // Add markers to the map.
  for (const station of stationData) {
    // Create a DOM element for each marker.
    const el = document.createElement("div");
    el.className = "marker";
    el.style.backgroundImage = `url(/images/red-dot.png)`;
    el.style.width = `32px`;
    el.style.height = `32px`;
    el.style.backgroundSize = "100%";

    // Add markers to the map.
    new mapboxgl.Marker(el)
      .setLngLat([station.longitude, station.latitude])
      .setPopup(
        new mapboxgl.Popup({
          closeButton: false,
        }).setHTML(
          `<b>${station.station_name}</b><br>${station.street_address}<br>${station.city}, ${station.state} ${station.zip}<br>EV Connector Types: ${station.ev_connector_types}`
        )
      )
      .addTo(map); // add popup
  }
}

function init() {
  mapboxgl.accessToken = config.MAPBOX_API_KEY;
  const map = new mapboxgl.Map({
    container: "map", // container ID
    style: "mapbox://styles/mapbox/streets-v11", // style URL
    center: [-104.9903, 39.7392], // starting position [lng, lat] -- We'll want to center this on the user's default start location
    zoom: 8, // starting zoom
  });

  setMapMarkers(map);
}

